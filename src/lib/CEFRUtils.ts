export const getCEFRScore = (percentage: number): { level: CEFRLevel; score: number } => {
  if (percentage >= 90) return { level: 'C2', score: percentage };
  if (percentage >= 80) return { level: 'C1', score: percentage };
  if (percentage >= 70) return { level: 'B2', score: percentage };
  if (percentage >= 60) return { level: 'B1', score: percentage };
  if (percentage >= 40) return { level: 'A2', score: percentage };
  return { level: 'A1', score: percentage };
};
