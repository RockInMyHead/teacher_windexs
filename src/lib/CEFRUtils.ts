export type CEFRLevel = 'A1' | 'A2' | 'B1' | 'B2' | 'C1' | 'C2';

export const getCEFRLevelName = (cefrLevel: CEFRLevel): string => {
  const names: Record<CEFRLevel, string> = {
    A1: 'Элементарный (A1)',
    A2: 'Базовый (A2)',
    B1: 'Средний (B1)',
    B2: 'Выше среднего (B2)',
    C1: 'Продвинутый (C1)',
    C2: 'В совершенстве (C2)'
  };
  return names[cefrLevel];
};

